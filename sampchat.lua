		elseif text:match("(%d+) (%d+) (.+)|(.+)") then nID, types_warn, text, skip = text:match("(%d+) (%d+) (.+)|(.+)") return nID, types_warn, text
		elseif text:match("(%d+) (%d+) (.+)by (.+)") then nID, types_warn, text, skip = text:match("(%d+) (%d+) (.+)by (.+)") return nID, types_warn, text
		elseif not text:match("(%d+) (%d+) (.+)|(.*)") or not text:match("(%d+) (%d+) (.+)/(.*)") or not text:match("(%d+) (%d+) (.+)||(.*)") or not text:match("(%d+) (%d+) (.+)//(.*)") or not text:match("(%d+) (%d+) (.+)by (.+)") then
			nID, types_warn, text = text:match('(%d+) (%d+) (.+)')
			return nID, types_warn, text
		end
	elseif tape == 5 then -- WRITE
