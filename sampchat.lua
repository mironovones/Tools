		elseif text:match("(%d+) (%d+) (%d+) (.*)|(.*)") then nID, types_ban, vremya, NPrich, Skips = text:match("(%d+) (%d+) (%d+) (.*)|(.*)") return nID, types_ban, vremya, NPrich
