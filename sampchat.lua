			nID,skip_p1, count,skip_p2, text, skip = text:match("(%d+)(%s+)(%d+)(%s+)(.+)//(.+)") 
			return nID, count, text
		elseif text:match("(%d+)(%s+)(%d+)(%s+)(.+)/(.+)") then 
			nID,skip_p1, count,skip_p2, text, skip = text:match("(%d+)(%s+)(%d+)(%s+)(.+)/(.+)") 
			return nID, count, text
		elseif text:match("(%d+)(%s+)(%d+)(%s+)(.+)||(.+)") then 
			nID,skip_p1, count,skip_p2, text, skip = text:match("(%d+)(%s+)(%d+)(%s+)(.+)||(.+)") 
			return nID, count, text
		elseif text:match("(%d+)(%s+)(%d+)(%s+)(.+)|(.+)") then 
			nID,skip_p1, count,skip_p2, text, skip = text:match("(%d+)(%s+)(%d+)(%s+)(.+)|(.+)") return nID, count, text
		elseif text:match("(%d+)(%s+)(%d+)(%s+)(.+)|(.+)") then 
