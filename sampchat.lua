		elseif not text:match("(%d+)(%s+)(%d+)(%s+)(.+)//(.+)") or not text:match("(%d+)(%s+)(%d+)(%s+)(.+)/(.+)") or not text:match("(%d+)(%s+)(%d+)(%s+)(.+)||(.+)") or not text:match("(%d+)(%s+)(%d+)(%s+)(.+)|(.+)") or not text:match("(%d+)(%s+)(%d+)(%s+)(.+) by (.+)") then 
			if text:match("(%d+)(%s+)(%d+)(%s+)(.+)") then 
				nID,skip_p1, count,skip_p2, text = text:match("(%d+)(%s+)(%d+)(%s+)(.+)") 
				return nID, count, text
			end
			if text:match("(%d+)(.+)||(.+)") then
				nID, text, skip = text:match("(%d+)(.+)||(.+)") 
				return nID, 1, text
			elseif text:match("(%d+)(.+)|(.+)") then
				nID, text, skip = text:match("(%d+)(.+)|(.+)") 
				return nID, 1, text
			elseif text:match("(%d+)(.+)//(.+)") then
				nID, text, skip = text:match("(%d+)(.+)//(.+)") 
				return nID, 1, text
			elseif text:match("(%d+)(.+)/(.+)") then
				nID, text, skip = text:match("(%d+)(.+)/(.+)") 
				return nID, 1, text
			elseif text:match("(%d+)(.+) by (.+)") then
