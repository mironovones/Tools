function sampChatNavodka(text, tape) -- Конвертирование
	if tape == 1 then -- JAIL, BAN, SBAN
		if text:match("(%d+) (%d+) (.*)//(.*)") then nID, vremya, NPrich, Skips = text:match("(%d+) (%d+) (.*)//(.*)") return nID, vremya, NPrich
		elseif text:match("(%d+) (%d+) (.*)/(.*)") then nID, vremya, NPrich, Skips = text:match("(%d+) (%d+) (.*)/(.*)") return nID, vremya, NPrich
		elseif text:match("(%d+) (%d+) (.*)||(.*)") then nID, vremya, NPrich, Skips = text:match("(%d+) (%d+) (.*)||(.*)") return nID, vremya, NPrich
		elseif text:match("(%d+) (%d+) (.*)|(.*)") then nID, vremya, NPrich, Skips = text:match("(%d+) (%d+) (.*)|(.*)") return nID, vremya, NPrich
		elseif text:match("(%d+) (%d+) (.*) by (.+)") then nID, vremya, NPrich, Skips = text:match("(%d+) (%d+) (.*) by (.*)") return nID, vremya, NPrich
		elseif not text:match("(%d+) (%d+) (.*)/(.*)") or not text:match("(%d+) (%d+) (.*)//(.*)")  or not text:match("(%d+) (%d+) (.*)||(.*)")  or not text:match("(%d+) (%d+) (.*)|(.*)") or not text:match("(%d+) (%d+) (.*) by (.+)") then
			nID,vremya, NPrich = text:match('(%d+) (%d+) (.*)') return nID, vremya, NPrich end
	elseif tape == 2 then -- MSG
		if text:match("(.+)//(.+)") then text, skip = text:match("(.+)//(.+)") return text
		elseif text:match("(.+)/(.+)") then text, skip = text:match("(.+)/(.+)") return text
		elseif text:match("(.+)||(.+)") then text, skip = text:match("(.+)||(.+)") return text
		elseif text:match("(.+)|(.+)") then text, skip = text:match("(.+)|(.+)") return text
		elseif text:match("(.+) by (.+)") then text, skip = text:match("(.+) by (.+)") return text
		elseif not text:match("(.*)|(.*)") or not text:match("(.*)/(.*)") or not text:match("(.*)||(.*)") or not text:match("(.*)//(.*)") or not text:match("(.+) by (.+)") then
			text = text:match('(.*)')
			return text
		end
	elseif tape == 3 then -- UNWARN, SKICK, REMOVEJBL
		if text:match("(.*)(%p+)(%p+)(.*)") then vremya, NPrich, Skips = text:match("(.*)(%p+)(%p+)(.*)") return vremya
		elseif text:match("(.*)(%p+)(.*)") then  vremya, NPrich, Skips = text:match("(.*)(%p+)(.*)") return vremya
		elseif not text:match("(.*)(%p+)(.*)") or not text:match("(.*)(%p+)(%p+)(.*)") then nID = text:match('(.*)') return nID end
	elseif tape == 4 then -- WARN
		if text:match("(%d+) (%d+) (.+)//(.+)") then nID, types_warn, text, skip = text:match("(%d+) (%d+) (.+)//(.+)") return nID, types_warn, text
		elseif text:match("(%d+) (%d+) (.+)/(.+)") then nID, types_warn,  text, skip = text:match("(%d+) (%d+) (.+)/(.+)") return nID, types_warn, text
