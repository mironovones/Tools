function Helpcordis(id)
	local result, handle = sampGetCharHandleBySampPlayerId(id)
	if result then 
		local mx, my, mz = getCharCoordinates(handle)
		local distanceList = {}
		if #xyz >= 0 then
			for v in ipairs(xyz) do
				local dist = getDistanceBetweenCoords3d(xyz[v][1], xyz[v][2], xyz[v][3], mx, my, mz)
				table.insert(distanceList, {distanceList[#distanceList + 1], dist, v})
			end
			table.sort(distanceList, function( a, b ) return a[2] > b[2] end) -- Последний элемент массива будет с самой малой дистанцией
			local count = distanceList[#distanceList][3]
			xb, yb, zb =  xyz[count][1], xyz[count][2], xyz[count][3]
		end 
		return xb, yb, zb
	end 
end 